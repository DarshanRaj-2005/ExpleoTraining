mark = int(input("Enter the Mark:"))

if(mark > 90):
    print("Your Grade is O")

elif(mark > 80):
    print("Your Grade is A")

elif(mark > 70):
    print("Your Grade is B")

elif(mark > 60):
    print("Your Grade is C")

elif(mark > 50):
    print("Your Grade is D")

else:
    print("Your Grade is F")