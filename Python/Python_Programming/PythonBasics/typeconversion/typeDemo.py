#Int
a = 10
print(type(a))

#Float
b = 20.5
print(type(b))

#String
c = "jaggu"
print(type(c))

#String
d = '234'
print(type(d))

#List
list1 = [5,3.0,4,"New Delhi",7]
print(type(list1))
print(list1)

#Tuple
tuple1 = (10,20.25,'New Delhi')
print(type(tuple1))
print(tuple1)

#Set
set1 = {10,20.25,'New Delhi'}
print(type(set1))
print(set1)

#None Data Type
myVar = None
print(myVar)
print(type(myVar))

#Dictionaries
student = {"Name":"Jaggu", "age":23, "mail":"jaggu@gmail"}
print(type(student))
print(student)